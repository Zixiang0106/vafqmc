Array = jnp.ndarray
SpinWfn = Tuple[Array, Array]
Wfn = Union[Array, SpinWfn]
RuntimeState = Dict[str, Any]


# Trial interface (for VAFQMC trial integration):
# - calc_slov(walkers) -> (sign, log|ov|)
# - calc_force_bias(hamil, walkers, prop_data) -> force_bias
# - calc_local_energy(hamil, walkers) -> energies
# - get_rdm1() or calc_rdm1() or rdm1 -> spin-summed RDM for mean-field shift
# - init_walkers(n_walkers, key, noise=0.0) -> walkers (optional)
# - orthonormalize_walkers(walkers) -> walkers (optional)
# - bind_walkers(walkers, key=..., reinit=...) to initialize per-walker pools
# - update_tethered_samples(walkers) -> (handoff, sign_new, logov_new) (optional)


@dataclass
class PropagationData:
    hmf: Array
    exp_h1: Array
    vhs: Array
    mf_shifts: Array
    h0_prop: Array
    dt: float
    sqrt_dt: float


def _is_det_trial(trial: Any) -> bool:
    if isinstance(trial, (tuple, list)):
        return True
    return isinstance(trial, (jnp.ndarray, onp.ndarray))


def _is_custom_trial(trial: Any) -> bool:
    return not _is_det_trial(trial)


def _require_spin_det(trial: Wfn) -> None:
    if not _has_spin(trial):
        raise ValueError(
            "AFQMC expects spin-separated (up, down) determinant trial for "
            "built-in determinant mode."
        )


def _spin_sum_rdm(rdm: Array) -> Array:
    if rdm.ndim == 3:
        return rdm[0] + rdm[1]
    if rdm.ndim == 4:
        return rdm.sum(1)
    return rdm


def gaussian_logdens(_params: Any, fields: Any) -> Array:
    """Unnormalized log-density of N(0, I) over an arbitrary pytree."""
    vals = [jnp.real(x * jnp.conj(x)).sum() for x in tree_leaves(fields)]
    return -0.5 * jnp.sum(jnp.asarray(vals))


def extract_params(payload: Any) -> Mapping[str, Any]:
    """Extract flax params from checkpoint payload.

    Supports:
    - dict with key ``"params"``
    - train.py tuple checkpoints: (rng, (step, params, ...))
    - recursive fallback scan
    """
    queue = [payload]
    while queue:
        node = queue.pop(0)
        if isinstance(node, Mapping):
            if "params" in node:
                return node
            queue.extend(node.values())
            continue

        if isinstance(node, (tuple, list)):
            if len(node) == 2 and isinstance(node[1], (tuple, list)) and len(node[1]) >= 2:
                maybe_params = node[1][1]
                if isinstance(maybe_params, Mapping) and "params" in maybe_params:
                    return maybe_params
            queue.extend(node)

    raise ValueError("Cannot locate flax params in checkpoint payload.")


def normalize_ansatz_params(params: Mapping[str, Any]) -> Mapping[str, Any]:
    """Convert braket checkpoint params to ansatz.apply params if needed."""
    if not isinstance(params, Mapping) or "params" not in params:
        return params
    inner = params["params"]
    if isinstance(inner, Mapping) and "ansatz" in inner:
        return {"params": inner["ansatz"]}
    return params


def load_ansatz_cfg_from_hparams(hparams_path: str) -> Mapping[str, Any]:
    """Load ``ansatz`` mapping from a training ``hparams.yml`` file."""
    with open(hparams_path, "r", encoding="utf-8") as f:
        data = yaml.safe_load(f)

    if not isinstance(data, Mapping):
        raise ValueError(f"Expected mapping in {hparams_path}, got {type(data).__name__}.")

    ansatz_cfg = data.get("ansatz")
    if ansatz_cfg is None and isinstance(data.get("cfg"), Mapping):
        ansatz_cfg = data["cfg"].get("ansatz")
    if not isinstance(ansatz_cfg, Mapping):
        raise ValueError(
            f"Cannot find mapping key 'ansatz' in {hparams_path}. "
            "Expected training config generated by hafqmc.train*."
        )
    return ansatz_cfg


def mix_overlap_terms(
    sample_sign: Array,
    sample_logov: Array,
    sample_log_abs: Array,
    sample_phase: Array,
) -> tuple[Array, Array, Array]:
    """Combine per-sample overlaps into total sign/log-overlap/mix-weights."""
    log_terms = sample_logov + sample_log_abs
    phases = sample_sign * sample_phase

    shift = jnp.max(jnp.real(log_terms))
    scaled = phases * jnp.exp(log_terms - shift)
    total = jnp.sum(scaled)
    total_abs = jnp.abs(total)
    valid = total_abs > 0

    sign = jnp.where(valid, total / total_abs, 0.0 + 0.0j)
    logov = jnp.where(valid, shift + jnp.log(total_abs), -jnp.inf)
    weights = jnp.where(valid, scaled / total, jnp.zeros_like(scaled))
    return sign, logov, weights


def phaseless_from_ratio(ratio: Array) -> Array:
    """Apply the standard phaseless projection factor to a complex ratio."""
    theta = jnp.angle(ratio)
    fac = jnp.abs(ratio) * jnp.cos(theta)
    fac = jnp.real(fac)
    fac = jnp.where(jnp.isnan(fac), 0.0, fac)
    fac = jnp.where(fac < 0.0, 0.0, fac)
    return fac


def reject_outliers(
    samples: onp.ndarray,
    obs_index: int,
    *,
    m: float = 10.0,
    min_threshold: float = 1.0e-5,
) -> tuple[onp.ndarray, onp.ndarray]:
    """Reject outliers in ``samples`` by robust dispersion on selected column."""
    target = onp.asarray(samples[:, obs_index], dtype=onp.float64)
    median_val = onp.median(target)
    dist = onp.abs(target - median_val)
    mdev = onp.median(dist)
    q1, q3 = onp.percentile(target, [25, 75])
    iqr = q3 - q1
    normalized_iqr = iqr / 1.349
    dispersion = max(float(mdev), float(normalized_iqr), float(min_threshold))
    score = dist / dispersion
    mask = score < float(m)
    return samples[mask], mask


def blocking_analysis(
    weights: onp.ndarray,
    energies: onp.ndarray,
    *,
    neql: int = 0,
) -> tuple[float, Optional[float]]:
    """Weighted blocking analysis used in ad_afqmc."""
    weights = onp.asarray(weights, dtype=onp.float64).reshape(-1)
    energies = onp.asarray(energies, dtype=onp.float64).reshape(-1)
    if weights.size != energies.size:
        raise ValueError("weights and energies must have same length.")
    if weights.size == 0:
        return onp.nan, 0.0

    neql_i = max(int(neql), 0)
    weights = weights[neql_i:]
    energies = energies[neql_i:]
    if weights.size == 0:
        return onp.nan, 0.0

    weighted_energies = weights * energies
    wsum = weights.sum()
    if wsum <= 0.0:
        return float(onp.mean(energies)), 0.0
    mean_energy = float(weighted_energies.sum() / wsum)

    n_samples = weights.shape[0]
    block_sizes = onp.array([1, 2, 5, 10, 20, 50, 100, 200, 300, 400, 500, 1000, 10000])
    prev_error = 0.0
    plateau_error = None
    errors: list[float] = []

    for bsz in block_sizes[block_sizes < n_samples / 2.0]:
        n_blocks = int(n_samples // int(bsz))
        if n_blocks <= 1:
            continue

        blocked_weights = onp.zeros(n_blocks, dtype=onp.float64)
        blocked_energies = onp.zeros(n_blocks, dtype=onp.float64)
        for j in range(n_blocks):
            sl = slice(j * int(bsz), (j + 1) * int(bsz))
            blocked_weights[j] = weights[sl].sum()
            if blocked_weights[j] > 0.0:
                blocked_energies[j] = weighted_energies[sl].sum() / blocked_weights[j]
            else:
                blocked_energies[j] = 0.0

        v1 = blocked_weights.sum()
        if v1 <= 0.0:
            continue
        v2 = (blocked_weights**2).sum()
        mean_b = float((blocked_weights * blocked_energies).sum() / v1)
        denom = (v1 - v2 / v1) * (n_blocks - 1)
        if denom <= 0.0:
            continue
        var = (blocked_weights * (blocked_energies - mean_b) ** 2).sum() / denom
        error = float(onp.sqrt(max(var, 0.0)))
        errors.append(error)
        if error < 1.05 * prev_error and plateau_error is None:
            plateau_error = max(error, prev_error)
        prev_error = error

    if plateau_error is None:
        if errors:
            plateau_error = 2.0 * errors[0]
        else:
            plateau_error = 0.0
    return mean_energy, float(plateau_error)


def analyze_energy_blocks(
    block_energies: onp.ndarray,
    block_weights: Optional[onp.ndarray] = None,
    *,
    reject_energy_outliers: bool = True,
    outlier_m: float = 10.0,
    outlier_min_threshold: float = 1.0e-5,
    neql: int = 0,
) -> tuple[float, float, int]:
    """Post-process block energies with optional outlier rejection and blocking error."""
    energies = onp.asarray(block_energies, dtype=onp.float64).reshape(-1)
    if energies.size == 0:
        return onp.nan, 0.0, 0

    if block_weights is None:
        weights = onp.ones_like(energies)
    else:
        weights = onp.asarray(block_weights, dtype=onp.float64).reshape(-1)
        if weights.size != energies.size:
            raise ValueError("block_weights size mismatch with block_energies.")

    samples = onp.stack((weights, energies), axis=1)
    n_outliers = 0
    if reject_energy_outliers and samples.shape[0] > 3:
        samples_clean, _ = reject_outliers(
            samples,
            1,
            m=outlier_m,
            min_threshold=outlier_min_threshold,
        )
        n_outliers = int(samples.shape[0] - samples_clean.shape[0])
    else:
        samples_clean = samples

    if samples_clean.shape[0] == 0:
        mean = float(onp.mean(energies))
        err = float(onp.std(energies, ddof=1) / onp.sqrt(energies.size)) if energies.size > 1 else 0.0
        return mean, err, int(n_outliers)

    w = samples_clean[:, 0]
    e = samples_clean[:, 1]
    mean, err = blocking_analysis(w, e, neql=neql)
    if err is None or (not onp.isfinite(err)):
        err = float(onp.std(e, ddof=1) / onp.sqrt(e.size)) if e.size > 1 else 0.0
    return float(mean), float(err), int(n_outliers)


def _get_trial_rdm1(trial: Any) -> Array:
    if _is_custom_trial(trial):
        if hasattr(trial, "get_rdm1"):
            return trial.get_rdm1()
        if hasattr(trial, "calc_rdm1"):
            return trial.calc_rdm1()
        if hasattr(trial, "rdm1"):
            return trial.rdm1
        raise ValueError("Custom trial must provide get_rdm1/calc_rdm1/rdm1.")
    _require_spin_det(trial)
    rdm_t = calc_rdm(trial, trial)
    return _spin_sum_rdm(rdm_t)


def build_propagation_data(hamil: Hamiltonian, trial: Any, dt: float) -> PropagationData:
    h1 = hamil.h1e
    vhs = hamil.ceri
    v0 = 0.5 * calc_v0(vhs)

    rdm_t = _get_trial_rdm1(trial)
    vbar = jnp.einsum("kpq,pq->k", vhs, rdm_t)
    v1 = jnp.einsum("k,kpq->pq", vbar, vhs)

    h1_mod = h1 - v0 + v1
    mf_shifts = 1.0j * vbar
    h0_prop = hamil.enuc + 0.5 * jnp.sum(mf_shifts * mf_shifts)
    exp_h1 = jsp.linalg.expm(-0.5 * dt * h1_mod)

    return PropagationData(
        hmf=h1_mod,
        exp_h1=exp_h1,
        vhs=vhs,
        mf_shifts=mf_shifts,
        h0_prop=h0_prop,
        dt=dt,
        sqrt_dt=jnp.sqrt(dt),
    )


def calc_slov_batch(trial: Any, walkers: Any) -> Tuple[Array, Array]:
    if _is_custom_trial(trial):
        if hasattr(trial, "calc_slov"):
            return trial.calc_slov(walkers)
        if hasattr(trial, "calc_overlap"):
            ov = trial.calc_overlap(walkers)
            mag = jnp.abs(ov)
            sign = jnp.where(mag > 0, ov / mag, 0.0 + 0.0j)
            logov = jnp.where(mag > 0, jnp.log(mag), -jnp.inf)
            return sign, logov
        raise ValueError("Custom trial must provide calc_slov or calc_overlap.")

    _require_spin_det(trial)
    w_up, w_dn = walkers

    def single(wu, wd):
        return calc_slov(trial, (wu, wd))

    return jax.vmap(single)(w_up, w_dn)


def calc_rdm_batch(trial: Any, walkers: Any) -> Array:
    if _is_custom_trial(trial):
        if hasattr(trial, "calc_rdm"):
            return trial.calc_rdm(walkers)
        raise ValueError("Custom trial must provide calc_rdm for RDM evaluation.")

    _require_spin_det(trial)
    w_up, w_dn = walkers

    def single(wu, wd):
        return calc_rdm(trial, (wu, wd))

    return jax.vmap(single)(w_up, w_dn)


def calc_local_energy_batch(hamil: Hamiltonian, trial: Any, walkers: Any) -> Array:
    if _is_custom_trial(trial):
        if hasattr(trial, "calc_local_energy"):
            return trial.calc_local_energy(hamil, walkers)
        if hasattr(trial, "calc_energy"):
            return trial.calc_energy(hamil, walkers)
        raise ValueError("Custom trial must provide calc_local_energy or calc_energy.")

    _require_spin_det(trial)
    w_up, w_dn = walkers

    def single(wu, wd):
        return hamil.local_energy(trial, (wu, wd))

    return jax.vmap(single)(w_up, w_dn)


def calc_force_bias(hamil: Hamiltonian, trial: Any, walkers: Any, prop_data: PropagationData) -> Array:
    if _is_custom_trial(trial) and hasattr(trial, "calc_force_bias"):
        return trial.calc_force_bias(hamil, walkers, prop_data)

    rdm = calc_rdm_batch(trial, walkers)
    rdm_sum = _spin_sum_rdm(rdm)
    return jnp.einsum("kpq,wpq->wk", prop_data.vhs, rdm_sum)


def apply_trotter(walkers: Wfn, shifted_fields: Array, prop_data: PropagationData) -> Wfn:
    if _has_spin(walkers):
        w_up, w_dn = walkers

        w_up = jnp.einsum("ij,wjk->wik", prop_data.exp_h1, w_up)
        w_dn = jnp.einsum("ij,wjk->wik", prop_data.exp_h1, w_dn)

        vhs_sum = jnp.tensordot(shifted_fields, prop_data.vhs, axes=1)
        vhs_sum = 1.0j * prop_data.sqrt_dt * vhs_sum
        if not jnp.issubdtype(vhs_sum.dtype, jnp.complexfloating):
            vhs_sum = vhs_sum.astype(jnp.complex128)
        w_up = w_up.astype(vhs_sum.dtype)
        w_dn = w_dn.astype(vhs_sum.dtype)
        w_up = jax.vmap(lambda a, b: DEFAULT_EXPM(a, b))(vhs_sum, w_up)
        w_dn = jax.vmap(lambda a, b: DEFAULT_EXPM(a, b))(vhs_sum, w_dn)

        w_up = jnp.einsum("ij,wjk->wik", prop_data.exp_h1, w_up)
        w_dn = jnp.einsum("ij,wjk->wik", prop_data.exp_h1, w_dn)
        return (w_up, w_dn)

    # GHF walker path: apply block-diagonal one-body/two-body operators.
    walkers_ghf = walkers
    nrow = int(walkers_ghf.shape[1])
    nao = int(prop_data.exp_h1.shape[0])

    if nrow not in (nao, 2 * nao):
        raise ValueError(
            f"Unsupported GHF walker row dimension {nrow}; expected {nao} or {2 * nao}."
        )

    exp_h1 = prop_data.exp_h1
    vhs_sum = jnp.tensordot(shifted_fields, prop_data.vhs, axes=1)  # [w, nao, nao]

    if nrow == 2 * nao:
        z_h1 = jnp.zeros_like(exp_h1)
        exp_h1 = jnp.block([[exp_h1, z_h1], [z_h1, exp_h1]])

        z_v = jnp.zeros_like(vhs_sum)
        top = jnp.concatenate([vhs_sum, z_v], axis=2)
        bot = jnp.concatenate([z_v, vhs_sum], axis=2)
        vhs_sum = jnp.concatenate([top, bot], axis=1)

    walkers_ghf = jnp.einsum("ij,wjk->wik", exp_h1, walkers_ghf)
    vhs_sum = 1.0j * prop_data.sqrt_dt * vhs_sum
    if not jnp.issubdtype(vhs_sum.dtype, jnp.complexfloating):
        vhs_sum = vhs_sum.astype(jnp.complex128)
    walkers_ghf = walkers_ghf.astype(vhs_sum.dtype)
    walkers_ghf = jax.vmap(lambda a, b: DEFAULT_EXPM(a, b))(vhs_sum, walkers_ghf)
    walkers_ghf = jnp.einsum("ij,wjk->wik", exp_h1, walkers_ghf)
    return walkers_ghf


def save_hamiltonian(hamil: Hamiltonian, path: str) -> None:
    save_pickle(path, hamil.to_tuple())


def load_hamiltonian(path: str) -> Hamiltonian:
    h1e, ceri, enuc, wfn0, aux = load_pickle(path)
    return Hamiltonian(h1e, ceri, enuc, wfn0, aux)


def build_hamiltonian_pickle(
    mf: Any,
    path: str,
    *,
    chol_cut: float = 1e-6,
    orth_ao: Optional[Any] = None,
    full_eri: bool = False,
    with_cc: bool = False,
    with_ghf: bool = False,
) -> Hamiltonian:
    hamil = Hamiltonian.from_pyscf(
        mf,
        chol_cut=chol_cut,
        orth_ao=orth_ao,
        full_eri=full_eri,
        with_cc=with_cc,
        with_ghf=with_ghf,
    )
    save_hamiltonian(hamil, path)
    return hamil
