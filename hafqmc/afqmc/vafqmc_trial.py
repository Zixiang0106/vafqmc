            replace_per_refresh=replace_per_refresh,
            sample_update_steps=sample_update_steps,
            init_walkers_from_trial=init_walkers_from_trial,
            init_walkers_burn_in=init_walkers_burn_in,
            pure_eval_pairs=pure_eval_pairs,
            max_prop=max_prop,
            seed=seed,
